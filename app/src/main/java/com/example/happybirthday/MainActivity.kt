class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            HappyBirthdayTheme {
                // A surface container using the 'background' color from the theme
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {
                    GreetingImage(from = getString(R.string.signature_text), message = getString(R.string.happy_birthday_text))
                }
            }
        }
    }
}

@Composable
fun GreetingText(from: String, message: String, modifier: Modifier = Modifier){
    Column(modifier = modifier.padding(8.dp), verticalArrangement = Arrangement.Center){
        Text(
            text = message,
            fontSize = 90.sp,
            lineHeight = 106.sp,
            textAlign = TextAlign.Center
        )
        Text(
            text = from,
            fontSize = 36.sp,
            modifier = Modifier
                .padding(16.dp)
                .align(alignment = Alignment.CenterHorizontally)

            )
    }
}

@Composable
fun GreetingImage(from: String, message: String, modifier: Modifier = Modifier){
    val image = painterResource(R.drawable.androidparty)
    Box {
        Image(
            painter = image,
            contentDescription = null,
            contentScale = ContentScale.Crop,
            alpha = 0.5F
        )
        GreetingText(
            from = from,
            message = message,
            modifier = Modifier
                .fillMaxSize()
                .padding(8.dp)
        )
    }
}

